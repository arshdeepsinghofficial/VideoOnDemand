class Video {

    String id;
    String cphoto;
    String sphoto;
    String producer;
    String director;
    String story_line;
    String genre;
    String Movietitle;
    String Videopath;
    String tpath;
    String duration;
    String language;
    String rating;

    public Video(String id, String cphoto, String sphoto, String producer, String director, String story_line, String genre, String Movietitle, String Videopath, String tpath, String duration, String language, String rating) {
        this.id = id;
        this.cphoto = cphoto;
        this.sphoto = sphoto;
        this.producer = producer;
        this.director = director;
        this.story_line = story_line;
        this.genre = genre;
        this.Movietitle = Movietitle;
        this.Videopath = Videopath;
        this.tpath = tpath;
        this.duration = duration;
        this.language = language;
        this.rating = rating;
    }

}